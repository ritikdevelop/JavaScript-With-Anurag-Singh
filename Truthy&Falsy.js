

//! Truthy and Falsy in javascript
// Truthy values:
//   - 1
//   - "Ritik"
//   - true
//   - [1, 2, 3]
//   - {a: 1, b: 2}

// Falsy values:
//   - 0
//   - ""
//   - undefined
//   - null
//   - NaN

const a = 5;
const b = 0;

// This will print truthy
console.log(Boolean(a));

// This will print falsy
console.log(Boolean(b));